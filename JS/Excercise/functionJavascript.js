/**
 * function 1 dan 2 menggunakan penulisan lama
 */
function jalanSatu() {
    console.log("jalan satu")
}
const jalanDua = function () {
    console.log("jalan dua");
}
jalanSatu();
jalanDua();
/**
 * function menggunakan penulisan baru / arrow function 
 */
const jalanTiga = ()=>{
    console.log("jalan tiga");
}
jalanTiga();